#Caracteres especiales en Cadenas

#Salto de linea
print ("Pepito \nPerez ")   #Este caracter permite realizar un salto de linea dentro de una cadena 

#Tabulacion 
print("\t Pepito Perez")  #Este caracter permite realizar una tabulacion antes de imprimir el mensaje

#Comilla simple 
print ('Pepito \' Perez')  #Este caracter permite Imprimr una comilla simple

#Comilla doble 
print ('Pepito \" Perez')  #Este caracter permite Imprimr una comilla doble

#Barra invertida 
print ('Pepito \\ Perez')  #Este caracter permite Imprimr una comilla doble 